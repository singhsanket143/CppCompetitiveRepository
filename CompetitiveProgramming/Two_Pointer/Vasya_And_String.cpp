ll maxBeauty(string str, int k, char ch) {
	ll count = 0;
	ll ans = INT_MIN;
	ll l = 0, r = 0;
	while(r < str.size()) {
		while(r < str.size() and str[r] == ch) {
			ans = max(ans, r-l+1);
			r++;
		}
		while(r < str.size() and str[r] != ch and count < k) {
			count++;
			ans = max(ans, r-l+1);
			r++;
		}
		while(l <= r and str[r] != ch and count >= k) {
			if(k == 0) {
				l = ++r;
				break;
			}
			count -= (str[l] != ch);
			l++;
		}
	}
	return ans;
}

