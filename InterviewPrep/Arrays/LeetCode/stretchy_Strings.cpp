int getConsecutiveLen(string &s, int start) {
	int end = start;
	while(end < s.size() and s[end] == s[start]) end++;
	return end - start;
}

bool isStretchy(string original, string stretchy) {
	int i = 0, j = 0;
	while(i < original.size() and j < stretchy.size()) {
		if(original[i] != stretchy[j]) {
			return false;
		}
		// heello, heellllo
		// extracting next set of consecutive characters
		int consecutive_orig = getConsecutiveLen(original, i);
		int consecutive_stretchy = getConsecutiveLen(stretchy, j);
		if((consecutive_orig > consecutive_stretchy or consecutive_stretchy < 3) 
			and consecutive_orig != consecutive_stretchy) {
			// violation condition
			return false;
		}
		i += consecutive_orig;
		j += consecutive_stretchy;
	}
	return i == original.size() and j == stretchy.size();
}

int countOfStretchy(string s, vector<string> &words) {
	int count = 0;
	for(string &str: words) {
		if(isStretchy(str, s)) {
			count++;
		}
	}
	return count;
}

